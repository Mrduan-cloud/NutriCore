# 用到了「手掌法则」量词的食材,需要在方案里附一行说明,否则"掌心/拳"反而费解
_HAND_UNITS = {"掌心", "拳", "瓷勺"}


def _fmt_count(n: float, unit: str) -> str:
    n = round(n * 2) / 2  # 取到 0.5,避免"0.73 个"这种反直觉数值
    if n <= 0:
        n = 0.5
    if abs(n - 0.5) < 1e-9:
        return f"半{unit}"
    if n == int(n):
        return f"{int(n)}{unit}"
    return f"{n:g}{unit}"


def _match(name: str) -> tuple[float, str] | None:
    spec = _UNIT.get(name)
    if spec:
        return spec
    # 名字可能含修饰(如"水煮西兰花""蒸南瓜"),做包含匹配(取最长键优先,更精确)
    for key in sorted(_UNIT, key=len, reverse=True):
        if key in name:
            return _UNIT[key]
    return None


def portion_hint(name: str, grams: float | None) -> str:
    """返回家常量词,如 "1个" / "1碗" / "2拳";无法换算时返回空串。"""
    if not name or not grams:
        return ""
    spec = _match(name)
    if not spec:
        return ""
    per, unit = spec
    return _fmt_count(grams / per, unit)


def uses_hand_estimate(name: str) -> bool:
    """该食材是否用到了「手掌法则」量词(掌心/拳/瓷勺)。"""
    spec = _match(name)
    return bool(spec and spec[1] in _HAND_UNITS)
